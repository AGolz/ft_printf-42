#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	int		len;
	char	*dst;

	len = 0;
	while (str[len])
		len++;
	dst = malloc(sizeof(char) * (len + 1));
	if (!dst)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dst[i] = str[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
